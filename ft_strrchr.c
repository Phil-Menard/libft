#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	len = ft_strlen(s);
	s += len;
	while (len >= 0)
	{
		if (*s == (char) c)
			return ((char *)s);
		s--;
		len--;
	}
	return (NULL);
}
